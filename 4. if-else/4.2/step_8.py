

n = int(input())
number_of_day = n % 7

if number_of_day == 1:
    print('понедельник')
elif number_of_day == 2:
    print('вторник')
elif number_of_day == 3:
    print('среда')
elif number_of_day == 4:
    print('четверг')
elif number_of_day == 5:
    print('пятница')
elif number_of_day == 6:
    print('суббота')
elif number_of_day == 7:
    print('воскресенье')


